    public void validateField() {
        String customerEmail=txtEmail.getText();
        String review=txtReview.getText();
        int spinner=(int) jSpinner2.getValue();
        if(!customerEmail.equals("") && customerEmail.matches(emailPattern) && spinner>-1)
            btn_save.setEnabled(true);
        else
            btn_save.setEnabled(false);
    }
    
    public void productNameByCategory(String category){
        DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> list=ProductDao.getAllRecordsByCategory(category);
        Iterator<Product> it=list.iterator();
        while(it.hasNext()){
            Product productObj=it.next();
            dtm.addRow(new Object[]{productObj.getName()});
        }
    }

