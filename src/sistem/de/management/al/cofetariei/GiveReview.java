    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        int rating=(int) jSpinner2.getValue();
        String customerEmail=txtEmail.getText();
        String productName=txtProName.getText();
        String userReview=txtReview.getText();
        Review review=new Review();
        review.setEmail(customerEmail);
        review.setProductName(productName);
        review.setRating(rating);
        review.setReview(userReview);
        ReviewDao.save(review);
        setVisible(false);
        new GiveReview().setVisible(true);
    }//GEN-LAST:event_btn_saveActionPerformed

